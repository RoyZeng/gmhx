package com.gome.gmhx.entity;

import java.util.Date;

import org.springframework.format.annotation.DateTimeFormat;

public class HxPosition {

    private String positionName;
    
    private String positionType;//岗位类型(0普通岗 1总部物料岗 2分部物料岗 3网点物料岗)
    
    private String fromType;//"0" 系统自建岗位  “1” 身份管理平台岗位
    
    private String orgId;
    
    private String positionCode;
    
    private String positionOrigin;
    
    private String positionOrgName;
    
    private String positionCategoryId;
    
    private String roleId;//岗位关联的角色id
    
    private String roleName;
    
    @DateTimeFormat(pattern="yyyy-MM-dd HH:mm:ss")
    private Date modifyDate;
    
    private String oldPositionCode;
    
	public String getPositionName() {
		return positionName;
	}

	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}

	public String getPositionType() {
		return positionType;
	}

	public void setPositionType(String positionType) {
		this.positionType = positionType;
	}

	public String getOrgId() {
		return orgId;
	}

	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}

	public String getFromType() {
		return fromType;
	}

	public void setFromType(String fromType) {
		this.fromType = fromType;
	}

	public String getPositionCode() {
		return positionCode;
	}

	public void setPositionCode(String positionCode) {
		this.positionCode = positionCode;
	}

	public String getPositionOrigin() {
		return positionOrigin;
	}

	public void setPositionOrigin(String positionOrigin) {
		this.positionOrigin = positionOrigin;
	}

	public String getPositionOrgName() {
		return positionOrgName;
	}

	public void setPositionOrgName(String positionOrgName) {
		this.positionOrgName = positionOrgName;
	}

	public String getPositionCategoryId() {
		return positionCategoryId;
	}

	public void setPositionCategoryId(String positionCategoryId) {
		this.positionCategoryId = positionCategoryId;
	}

	public Date getModifyDate() {
		return modifyDate;
	}

	public void setModifyDate(Date modifyDate) {
		this.modifyDate = modifyDate;
	}

	public String getOldPositionCode() {
		return oldPositionCode;
	}

	public void setOldPositionCode(String oldPositionCode) {
		this.oldPositionCode = oldPositionCode;
	}

	public String getRoleId() {
		return roleId;
	}

	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	
}